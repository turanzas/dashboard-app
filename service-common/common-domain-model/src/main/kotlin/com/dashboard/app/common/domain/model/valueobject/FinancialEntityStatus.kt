package com.dashboard.app.common.domain.model.valueobject

/**
 * Enum class representing the status of a financial entity.
 *
 * @property ACTIVE Indicates that the financial entity is active and can be used.
 * @property INACTIVE Indicates that the financial entity is inactive and cannot be used.
 */
enum class FinancialEntityStatus {
    ACTIVE,
    INACTIVE
}